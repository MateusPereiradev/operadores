larg= float(input('Digite aqui a largura da parede:'))
alt= float(input('Digite aqui a altura da parede:'))
area= larg*alt
print(f'Sendo a largura da parede {larg} e a altura {alt} a area em metros quadros é {area:.2f}m²')
tinta=area/2
print(f'Sendo a area da parede {area}m² será preciso {tinta}L de tinta para pintar a parede toda.')

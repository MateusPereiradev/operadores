produto= float(input('Digite aqui o valor do produto:'))
desconto= produto*5/100
prazo= produto*8/100
print(f'Referente ao preço do produto {produto:.2f} o valor com 5% de desconto é {produto-desconto:.2f}')
print(f'Referente ao preço do produto {produto:.2f} o valor com 8% de acrescimo é {produto+prazo:.2f}')
# mostre o valor de um produto com 5% de desconto e outro valor a prazo com 8% de acréscimo em cima do valor do produto.
